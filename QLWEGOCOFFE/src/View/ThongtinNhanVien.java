    public void showThongtinNhanVien(){
        try{
            DBConnect a= new DBConnect();
            Connection conn = a.getConnnection();
            int number;
            Vector row,column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM NHANVIEN");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            for(int i=1;i<=number;i++){
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++){
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                tbNhanVien.setModel(tbn);
            }
        tbNhanVien.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(tbNhanVien.getSelectedRow()>=0){
                        tfMaNV.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 0)+ "");
                        tfTenNV.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 1)+ "");
                        tfNgaysinh.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 2)+ "");
                        tfGioitinh.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 3)+ "");
                        tfDiachi.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 4)+ "");
                        tfSDT.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 5)+ "");
                        tfEmail.setText(tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 6)+ "");
                    }
                }
            
        });
        }catch(SQLException ex){
            System.out.println(ex.toString());
        }
    }

