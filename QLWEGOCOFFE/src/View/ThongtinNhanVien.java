    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            DBConnect a = new DBConnect();
            Connection conn = a.getConnnection();
            PreparedStatement ps = conn.prepareStatement("DELETE NHANVIEN WHERE MANV=?");
            ps.setString(1, tbNhanVien.getValueAt(tbNhanVien.getSelectedRow(), 0).toString() );
            if( JOptionPane.showConfirmDialog(this,"DELETE THIS NHANVIEN?", "Confirm",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                ps.executeUpdate();
                tbn.setRowCount(0);
                showThongtinNhanVien();
            }   
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    public boolean checkValidateForm(){
        if(tfMaNV.getText().isEmpty()|| tfTenNV.getText().isEmpty()|| tfNgaysinh.getText().isEmpty()||tfGioitinh.getText().isEmpty()||tfDiachi.getText().isEmpty()||tfSDT.getText().isEmpty()||tfEmail.getText().isEmpty()){
           return false; 
        }
        return true;
    }
    
