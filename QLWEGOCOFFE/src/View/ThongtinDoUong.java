            DBConnect a = new DBConnect();
            Connection conn = a.getConnnection();
            PreparedStatement ps = conn.prepareStatement("DELETE THUCUONG WHERE MATU=?");
            ps.setString(1, tbThongtinDoUong.getValueAt(tbThongtinDoUong.getSelectedRow(), 0).toString() );
