    public void showThongtinDoUong(){
        try{
            DBConnect a= new DBConnect();
            Connection conn = a.getConnnection();
            int number;
            Vector row,column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM THUCUONG");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            for(int i=1;i<=number;i++){
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++){
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                tbThongtinDoUong.setModel(tbn);
            }
        tbThongtinDoUong.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(tbThongtinDoUong.getSelectedRow()>=0){
                        tfMathucuong.setText(tbThongtinDoUong.getValueAt(tbThongtinDoUong.getSelectedRow(), 0)+ "");
                        tfMaloai.setText(tbThongtinDoUong.getValueAt(tbThongtinDoUong.getSelectedRow(), 1)+ "");
                        tfTenthucuong.setText(tbThongtinDoUong.getValueAt(tbThongtinDoUong.getSelectedRow(), 2)+ "");
                        tfDongia.setText(tbThongtinDoUong.getValueAt(tbThongtinDoUong.getSelectedRow(), 3)+ "");
                    }
                }
            
        });
        }catch(SQLException ex){
            System.out.println(ex.toString());
        }   
    }
    
