    public void showKhuvucuong(){
        try{
            DBConnect a= new DBConnect();
            Connection conn = a.getConnnection();
            int number;
            Vector row,column;
            column = new Vector();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM KHUVUC");
            ResultSetMetaData metadata = rs.getMetaData();
            number = metadata.getColumnCount();
            for(int i=1;i<=number;i++){
                column.add(metadata.getColumnName(i));
            }
            tbn.setColumnIdentifiers(column);
            while(rs.next()){
                row = new Vector();
                for(int i=1;i<=number;i++){
                    row.addElement(rs.getString(i));
                }
                tbn.addRow(row);
                tbKhuvucuong.setModel(tbn);
            }
        tbKhuvucuong.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if(tbKhuvucuong.getSelectedRow()>=0){
                        tfMaKV.setText(tbKhuvucuong.getValueAt(tbKhuvucuong.getSelectedRow(), 0)+ "");
                        tfTenKV.setText(tbKhuvucuong.getValueAt(tbKhuvucuong.getSelectedRow(), 1)+ "");
                        tfHesogia.setText(tbKhuvucuong.getValueAt(tbKhuvucuong.getSelectedRow(), 2)+ "");
                    }
                }
            
        });
        }catch(SQLException ex){
            System.out.println(ex.toString());
        }
    }

