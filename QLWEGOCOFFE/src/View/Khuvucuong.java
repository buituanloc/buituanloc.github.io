    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            DBConnect a = new DBConnect();
            Connection conn = a.getConnnection();  
            PreparedStatement comm = conn.prepareStatement("UPDATE KHUVUC SET MAKV=?,TENKV=?,HESOGIA=? WHERE MAKV=?");
            comm.setString(3, tfMaKV.getText());
            comm.setString(1, tfTenKV.getText());
            comm.setString(2, tfHesogia.getText());
            comm.executeUpdate();
            tbn.setRowCount(0);
            showKhuvucuong();
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            DBConnect a = new DBConnect();
            Connection conn = a.getConnnection();
            PreparedStatement ps = conn.prepareStatement("DELETE KHUVUC WHERE MAKV=?");
            ps.setString(1, tbKhuvucuong.getValueAt(tbKhuvucuong.getSelectedRow(), 0).toString() );
            if( JOptionPane.showConfirmDialog(this,"DELETE THIS KHUVUC?", "Confirm",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                ps.executeUpdate();
                tbn.setRowCount(0);
                showKhuvucuong();
            }   
        }catch(Exception ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
